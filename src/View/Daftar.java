    private void daftarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_daftarButtonActionPerformed
    validateKosong();
        if (validateRegister()) {
            User user = new User();
            String id = "11";
            Random random = new Random();
            for(int i = 0; i < 3; i++)
            id += random.nextInt(10);
            user.setIdUser(id);

            user.setNama(userNameTextField.getText());
            user.setEmail(emailTextField.getText());
            user.setNoTel(noTelTextField.getText());
            user.setPassword(String.valueOf(passwordField.getPassword()));
            if (String.valueOf(passwordField.getPassword()).equals(String.valueOf(konfirmasiField.getPassword()))) {
                try {
                    Database.getInstance().insertUser(user);
                    JOptionPane.showMessageDialog(this, "Berhasil Mendaftarkan User");
                    clearFormDaftar();
                    new Login().setVisible(true);
                    this.dispose();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Gagal Mendaftarkan user");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Konfirmasi Password Salah");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Terdapat Error, Silakan Perbaiki !");
        }
    }//GEN-LAST:event_daftarButtonActionPerformed

