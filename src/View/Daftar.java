    public void validateKosong() {
        if (userNameTextField.getText().equals("")) {
            errorUserName.setText("Jangan Kosong");
        }

        if (emailTextField.getText().equals("")) {
            errorEmail.setText("Jangan Kosong");
        }
        
        if (noTelTextField.getText().equals("")) {
            errorKonfirmasi.setText("Jangan Kosong");
        }
        if (Arrays.toString(passwordField.getPassword()).equals("[]")) {
            errorNoTel.setText("Jangan Kosong");
        }
        if (Arrays.toString(konfirmasiField.getPassword()).equals("[]")) {
            errorPassword.setText("Jangan Kosong");
        }
    }

    public boolean validateRegister(){
        String nama = errorUserName.getText();
        String email = errorEmail.getText();
        String noTel = errorKonfirmasi.getText();
        String password = errorNoTel.getText();
        String konfirmasi = errorPassword.getText();
        
        return nama.equals("")&&email.equals("")&&noTel.equals("")
                &&password.equals("")&&konfirmasi.equals("");
    }
    
    private void clearFormDaftar() {
        userNameTextField.setText("");
        emailTextField.setText("");
        noTelTextField.setText("");
        passwordField.setText("");
        konfirmasiField.setText("");
    }

